package practiespojocls;

public class Std  {
public String name;
public String age;
public Std() {}
public Std(String name,String age) {
	this.name=name;
	this.age=age;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getAge() {
	return age;
}
public void setAge(String age) {
	this.age = age;
}


}
